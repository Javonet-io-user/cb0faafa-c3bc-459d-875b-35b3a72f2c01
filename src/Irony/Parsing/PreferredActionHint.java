public class PreferredActionHint extends GrammarHint {
  protected NObject javonetHandle;

  public PreferredActionHint(PreferredActionType actionType) {
    super((NObject) null);
    try {
      javonetHandle =
          Javonet.New("Irony.Parsing.PreferredActionHint", NEnum.fromJavaEnum(actionType));
      super.setJavonetHandle(javonetHandle);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
    }
  }

  public PreferredActionHint(NObject handle) {
    super(handle);
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  @MethodTypeAnnotation(type = "Method")
  public void Apply(LanguageData language, LRItem owner) {
    try {
      javonetHandle.invoke("Apply", language, owner);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
